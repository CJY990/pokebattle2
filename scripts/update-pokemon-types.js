const fs = require('fs');
const path = require('path');
const https = require('https');

const CARDS_PATH = path.join(__dirname, '../src/data/cards.ts');
const OUTPUT_PATH = path.join(__dirname, '../src/data/pokemonTypes.ts');

// Extract IDs from cards.ts
const cardsContent = fs.readFileSync(CARDS_PATH, 'utf-8');
const idRegex = /id:\s*['"](\d+)['"]/g;
const ids = [];
let match;
while ((match = idRegex.exec(cardsContent)) !== null) {
    if (!ids.includes(match[1])) {
        ids.push(match[1]);
    }
}

console.log(`Found ${ids.length} Pokemon IDs. Fetching types...`);

function fetchUrl(url) {
    return new Promise((resolve, reject) => {
        https.get(url, (res) => {
            let data = '';
            res.on('data', chunk => data += chunk);
            res.on('end', () => {
                if (res.statusCode >= 200 && res.statusCode < 300) {
                    try {
                        resolve(JSON.parse(data));
                    } catch (e) { reject(e); }
                } else {
                    reject(new Error(`Status ${res.statusCode}`));
                }
            });
        }).on('error', reject);
    });
}

async function fetchTypes() {
    const typeMap = {};

    for (const id of ids) {
        try {
            const data = await fetchUrl(`https://pokeapi.co/api/v2/pokemon/${id}`);
            const type = data.types[0].type.name;
            typeMap[id] = type;
            console.log(`Fetched ${data.name} (${id}): ${type}`);

            // Simple rate limiting
            await new Promise(r => setTimeout(r, 100));
        } catch (e) {
            console.error(`Error fetching ${id}:`, e.message);
        }
    }

    const fileContent = `// Auto-generated by scripts/update-pokemon-types.js
export const POKEMON_TYPES: Record<string, string> = ${JSON.stringify(typeMap, null, 4)};
`;

    fs.writeFileSync(OUTPUT_PATH, fileContent);
    console.log(`Successfully wrote types to ${OUTPUT_PATH}`);
}

fetchTypes();
